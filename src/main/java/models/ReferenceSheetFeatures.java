package models;

public class ReferenceSheetFeatures {

    public int heigh;
    public int width;

    public ReferenceSheetFeatures(int heigh, int width) {
        this.heigh = heigh;
        this.width = width;
    }

    public int getHeigh() {
        return heigh;
    }

    public void setHeigh(int heigh) {
        this.heigh = heigh;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
